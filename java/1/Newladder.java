class Newladder {
    public static void main(String []args){
        for(int o=0;o<5;o++){
            for(int i=0;i<=5;i++){
                System.out.print(" ");
            }
            for(int j=5;j<=10;j++){
                System.out.print("$");
            }
            System.out.println("");
        }
    }
}
